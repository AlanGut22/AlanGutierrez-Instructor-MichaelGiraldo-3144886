public class Figura {
	public String nombre;
	public Integer numLados;
	public Double perimetro;
	public Double area;

	public Figura(String nombre, Integer numLados, Double perimetro, Double area) {
		this.nombre = nombre;
		this.numLados = numLados;
		this.perimetro = perimetro;
		this.area = area;
	}
}